    private void jButton_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ExcluirActionPerformed
        try {
            DefaultTableModel model = (DefaultTableModel) jTable_Resposta.getModel();
            int selectedRowIndex = jTable_Resposta.getSelectedRow();

            if (selectedRowIndex != -1) {
                //mostra uma caixa para confirmar exclusao do contato
                int resposta = JOptionPane.showConfirmDialog(this,
                    "Você tem certeza que quer excluir este contato?",
                    "Confirmação de Exclusão",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE);

                //pede a confirmação da exclusao
                if (resposta == JOptionPane.YES_OPTION) {
                    //converte o ID para int
                    int id = Integer.parseInt(model.getValueAt(selectedRowIndex, 0).toString());
                    ContatoDao dao = new ContatoDao();
                    ContatoControl contatoControl = new ContatoControl(dao);

                    contatoControl.excluir(id);

                    model.removeRow(selectedRowIndex);
                }
            } else {
                JOptionPane.showMessageDialog(this,
                    "Selecione um contato para excluir (Para isso, liste e selecione o contato a excluir. Caso não encontre, use a busca).",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton_ExcluirActionPerformed

