    private void jButton_ordenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ordenarActionPerformed
        try {
        //inicializa a tabela
        DefaultTableModel model = new DefaultTableModel(
            new Object[][] {},
            new String[] {"ID", "Nome", "Telefone", "Email", "Endereco"}
        );
        jTable_Resposta.setModel(model);

        //faz a recuperacao dos contatos
        ContatoDao dao = new ContatoDao();
        ContatoControl contatoControl = new ContatoControl(dao);
        LinkedList<Contato> contatosList = contatoControl.listar();

        // converte a lista encadeada em vetor
        Vector<Contato> contatos = new Vector<>(contatosList);

        //ordena 
        String criterioOrdenacao = (String) jComboBox_Ordenar.getSelectedItem();
        switch (criterioOrdenacao) {
            case "ID":
                contatos.sort(Comparator.comparingInt(Contato::getId));
                break;
            case "Celular":
                contatos.sort(Comparator.comparing(contato -> {
                    Telefone telefone = contato.getTelefone();
                    return String.format("%03d%03d%08d", telefone.getDdi(), telefone.getDdd(), telefone.getNumero());
                }));
                break;
            case "Email":
                contatos.sort(Comparator.comparing(Contato::getEmail, String.CASE_INSENSITIVE_ORDER));
                break;
            default:
                break;
        }

        //limpa a tabela
        model.setRowCount(0);

        //adiciona os contasto na tabela
        for (Contato contato : contatos) {
            String telefone = String.format("+%d (%d) %d",
                contato.getTelefone().getDdi(),
                contato.getTelefone().getDdd(),
                contato.getTelefone().getNumero()
            );
            String endereco = String.format("%s, %d, %s, %s, %s, %s",
                contato.getEndereco().getLogradouro(),
                contato.getEndereco().getNumero(),
                contato.getEndereco().getComplemento(),
                contato.getEndereco().getCep(),
                contato.getEndereco().getCidade(),
                contato.getEndereco().getEstado()
            );

            model.addRow(new Object[]{
                contato.getId(),
                contato.getNome(),
                telefone,
                contato.getEmail(),
                endereco
            });
        }
    } catch (Exception erro) {
        JOptionPane.showMessageDialog(this, "Erro: " + erro.getMessage());
    }
    }//GEN-LAST:event_jButton_ordenarActionPerformed

    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            String selectedItem = (String) jComboBox1.getSelectedItem();
            try {
                if ("Celular".equals(selectedItem)) {
                    MaskFormatter celularMask = new MaskFormatter("+## (##) #####-####");

                    jFormattedTextField1_consultar.setFormatterFactory(new DefaultFormatterFactory(celularMask));
                } else if ("Email".equals(selectedItem)) {
                    jFormattedTextField1_consultar.setFormatterFactory(null);
                    jFormattedTextField1_consultar.setText(""); 
                }
            } catch (ParseException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

