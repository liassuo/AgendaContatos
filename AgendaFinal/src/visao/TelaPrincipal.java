    private void jButton_ListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ListarActionPerformed
    try {
        DefaultTableModel model = new DefaultTableModel(
            new Object[][] {},
            new String[] {"Nome", "Telefone", "Email", "Endereco"}
        );
        jTable_Resposta.setModel(model);
        
        ContatoDao dao = new ContatoDao();
        ContatoControl contatoControl = new ContatoControl(dao);
        ArrayList<Contato> contatos = contatoControl.listar();
        
        for (Contato contato : contatos) {
            Object[] rowData = {
                contato.getNome(),
                contato.getTelefone().imprimirCelular(),
                contato.getEmail(),
                contato.getEndereco().imprimirEndereco()
            };
            model.addRow(rowData);
        }
    } catch (Exception erro) {
        JOptionPane.showMessageDialog(this, erro);
    }

    }//GEN-LAST:event_jButton_ListarActionPerformed

    private void jButton_ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ConsultarActionPerformed
        try{
            ContatoDao dao = new ContatoDao();
            ContatoControl contatoControl = new ContatoControl(dao);

            Contato contato = contatoControl.consultar(jTextField1_ConsultatCTT.getText().toUpperCase());


            DefaultTableModel model = (DefaultTableModel) jTable_Resposta.getModel();
            model.setRowCount(0); // Clear existing rows


            String telefoneStr = "+" + contato.getTelefone().getDdi() + " (" + contato.getTelefone().getDdd() + ") " + contato.getTelefone().getNumero();
            String enderecoStr = contato.getEndereco().getLogradouro() + ", " +
                                 contato.getEndereco().getNumero() + " " +
                                 contato.getEndereco().getComplemento() + ", " +
                                 contato.getEndereco().getCep() + ", " +
                                 contato.getEndereco().getCidade() + ", " +
                                 contato.getEndereco().getEstado();

        model.addRow(new Object[]{
            contato.getNome(),
            telefoneStr,
            contato.getEmail(),
            enderecoStr
        });

        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(this, "Contato não encontrado: " + jTextField1_ConsultatCTT.getText(), "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Erro ao ler o arquivo: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro);
        }
      
    }//GEN-LAST:event_jButton_ConsultarActionPerformed

    private void jButton_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ExcluirActionPerformed
        try {

            DefaultTableModel model = (DefaultTableModel) jTable_Resposta.getModel();

            int selectedRowIndex = jTable_Resposta.getSelectedRow();

            if (selectedRowIndex != -1) {

                String nome = model.getValueAt(selectedRowIndex, 0).toString();

                ContatoDao dao = new ContatoDao();
                ContatoControl contatoControl = new ContatoControl(dao);

                contatoControl.excluir(nome);

                model.removeRow(selectedRowIndex);
            } else {

                JOptionPane.showMessageDialog(this, "Selecione um contato para excluir(Para isso liste e selecione o contato a excluir caso nao encontre,use o buscar).", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        }catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro);
        }
    }//GEN-LAST:event_jButton_ExcluirActionPerformed

