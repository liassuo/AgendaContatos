    private void jTable_RespostaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_RespostaMouseClicked

               // Obtém o modelo da tabela
    DefaultTableModel model = (DefaultTableModel) jTable_Resposta.getModel();

    // Obtém o índice da linha selecionada
    int selectedRowIndex = jTable_Resposta.getSelectedRow();

    // Verifica se há uma linha selecionada
    if (selectedRowIndex != -1) {
        // Obtém os valores das colunas da linha selecionada
        String valor1 = model.getValueAt(selectedRowIndex, 0).toString();
        String valor2 = model.getValueAt(selectedRowIndex, 1).toString();
        String valor3 = model.getValueAt(selectedRowIndex, 2).toString();
        String valor4 = model.getValueAt(selectedRowIndex, 3).toString();
        String limpoValor = valor2.replaceAll("[^0-9]", "");
        String[] addressParts = valor4.split("/");
        String logradouro = addressParts[0].trim();
        String numero = addressParts[1].trim();
        String complemento = addressParts[2].trim();
        String cep = addressParts[3].replace("-", "").trim();
        String cidade = addressParts[4].trim();
        String estado = addressParts[5].trim();
        jTextField4_Logradouro.setText(logradouro);
        jTextField5_Numero.setText(numero);
        jTextField6_Complemento.setText(complemento);
        jFormattedTextField2_Cep.setText(cep);
        jTextField9_Estado.setText(estado);
        jTextField8_Cidade.setText(cidade);
        jTextField1_Nome.setText(valor1);
        jFormattedTextField1_Telefone.setText(limpoValor);
        jTextField3_Email.setText(valor3);
    }
    }//GEN-LAST:event_jTable_RespostaMouseClicked

