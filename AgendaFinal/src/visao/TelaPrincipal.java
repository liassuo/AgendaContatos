    private void jButton6_PDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6_PDFActionPerformed
        
        String nomeArquivo = "arquivo.csv";
        String caminhoRelativo = "src/arquivo/" + nomeArquivo;
        String pdfFile = "contato.pdf";

        try {
            try (CSVReader reader = new CSVReader(new FileReader(caminhoRelativo))) {
                String[] nextLine;
                Document document = new Document();
                PdfWriter.getInstance(document, new FileOutputStream(pdfFile));
                document.open();

                // Processamento dos dados e inclusão no PDF
                while ((nextLine = reader.readNext()) != null) {
                    for (String line : nextLine) {
                        Paragraph paragraph = new Paragraph(line);
                        document.add(paragraph);
                    }
                }

                // Fechamento do documento
                document.close();
            }

            // Abrir documento no leitor padrão do sistema
            if (Desktop.isDesktopSupported()) {
                Desktop.getDesktop().open(new File(pdfFile));
            } else {
                throw new Exception("Desktop não suportado. Não foi possível abrir o PDF automaticamente.");
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro);
        }
    }//GEN-LAST:event_jButton6_PDFActionPerformed

